// Função para exibir uma mensagem de boas-vindas ao carregar a página index.html
function exibirMensagemDeBoasVindas() {
    alert("Bem-vindo(a)!");
}

// Adicionando um listener ao evento "load" para chamar a função de boas-vindas ao carregar a página
window.addEventListener("load", exibirMensagemDeBoasVindas);